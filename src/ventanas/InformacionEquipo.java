    public InformacionEquipo() {
        initComponents();
        user = Login.user;
        user_update = GestionarClientes.user_update;
        equipo_update = InformacionCliente.equipo_update;
        

        setSize(641, 447);
        setResizable(false); 
        setTitle("Información del equipo " + " - Sesión de " + user);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(lbl_wallpaper.getWidth(),
                lbl_wallpaper.getHeight(), Image.SCALE_DEFAULT));
        lbl_wallpaper.setIcon(icono);
        this.repaint();
        
        jLabel1.setText("Información de Equipo");
        txt_nombreCliente.setText(user_update);
        
        
        //traemos la info de la bd de equipos
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select * from equipos where tipo_equipo = '" + equipo_update + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                ID = rs.getInt("id_equipo");
                
                cmb_tipoEquipo.setSelectedItem(rs.getString("tipo_equipo"));
                cmb_estatus.setSelectedItem(rs.getString("estatus"));
                cmb_marca.setSelectedItem(rs.getString("marca"));
                
                
                txt_modelo.setText(rs.getString("modelo"));
                txt_noSerie.setText(rs.getString("num_serie"));
                txt_ultmodificPor.setText(rs.getString("ultima_modificacion"));
                txt_fechaIngreso.setText(rs.getString("fecha_ingreso"));
                txt_observaciones.setText(rs.getString("observaciones"));
                
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en cargar cliente. " + e);
            JOptionPane.showMessageDialog(null, "¡¡ERROR al cargar!!, contacte al administrador.");
        }
    }
    
