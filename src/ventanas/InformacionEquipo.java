    private void jButton_actualizarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarEquipoActionPerformed

        int tipoEq_cmb, marca_cmb, estatus_cmb = 0, validacion = 0;
        String  modelo, noSerie, observaciones, tipoEquipo_string = "", Marca_string = "", estatus_string = "";

        noSerie = txt_noSerie.getText().trim();
        modelo = txt_modelo.getText().trim();
        marca_cmb = cmb_marca.getSelectedIndex() + 1;
        tipoEq_cmb = cmb_tipoEquipo.getSelectedIndex() + 1;
        estatus_cmb = cmb_estatus.getSelectedIndex() + 1;
        observaciones = txt_observaciones.getText().trim();

        if(noSerie.equals("")){
            txt_noSerie.setBackground(Color.red);
            validacion++;
        }
        if(modelo.equals("")){
            txt_modelo.setBackground(Color.red);
            validacion++;
        }
        if(observaciones.equals("")){
            txt_observaciones.setBackground(Color.red);
            validacion++;
        }

        if(marca_cmb == 1){
            Marca_string = "Apple";
        } else if(marca_cmb == 2){
            Marca_string = "Acer";
        } else if(marca_cmb == 3){
            Marca_string = "HP";
        } else if(marca_cmb == 4){
            Marca_string = "Lenovo";
        } else if(marca_cmb == 5){
            Marca_string = "Sony";
        }

        if(tipoEq_cmb == 1){
            tipoEquipo_string = "Laptop";
        } else if(tipoEq_cmb == 2){
            tipoEquipo_string = "Desktop";
        } else if(tipoEq_cmb == 3){
            tipoEquipo_string = "Impresora";
        } else if(tipoEq_cmb == 4){
            tipoEquipo_string = "Multifuncional";
        }
        
        if(estatus_cmb == 1){
            estatus_string = "Nuevo Ingreso";
        } else if(estatus_cmb == 2){
            estatus_string = "No Reparado";
        } else if(estatus_cmb == 3){
            estatus_string = "En Revision";
        } else if(estatus_cmb == 4){
            estatus_string = "Reparado";
        } else if(estatus_cmb == 5){
            estatus_string = "Entregado";
        }

        

        if (validacion == 0) {

            try{
                  
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("update equipos set tipo_equipo ='"+tipoEquipo_string+"',marca='"+Marca_string+"',modelo='"+txt_modelo.getText()+"',num_serie='"+txt_noSerie.getText()+"',"
                   +"estatus='"+estatus_string+"',ultima_modificacion='"+user+"',observaciones='"+txt_observaciones.getText()+"'where id_equipo ='"+ID+"'");
                 
                pst.executeUpdate();
                cn.close();
           
                System.out.println("Registro Exitoso");
                JOptionPane.showMessageDialog(null, "Registro exitoso.");
                this.dispose();
           
                            
            }catch(SQLException e){
                System.err.println("Error al Actualizar el usuario." + e);
                        JOptionPane.showMessageDialog(null, "¡¡ERROR al actualizar!!, contacte al administrador.");
            
        }

        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos.");
        }
    }//GEN-LAST:event_jButton_actualizarEquipoActionPerformed

